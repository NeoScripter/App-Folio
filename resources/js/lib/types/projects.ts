export interface ProjectResource {
    data: ProjectType[];
    links?: {
        first: string | null;
        last: string | null;
        prev: string | null;
        next: string | null;
    };
    meta?: {
        current_page: number;
        from: number | null;
        last_page: number;
        links: PaginationLink[];
        path: string;
        per_page: number;
        to: number | null;
        total: number;
    };
}

export type FluidImageType = {
    dkWebp: string;
    dkAvif: string;
    tbWebp: string;
    tbAvif: string;
    mbWebp: string;
    mbAvif: string;
    tiny: string;
    alt: {
        ru: string;
        en: string;
    };
};

export interface PaginationLink {
    url: string | null;
    label: string;
    active: boolean;
}

export interface PaginationMeta<T> {
    current_page: number;
    data: T[];
    first_page_url: string;
    from: number;
    last_page: number;
    last_page_url: string;
    links: PaginationLink[];
    next_page_url: string | null;
    path: string;
    per_page: number;
    prev_page_url: string | null;
    to: number;
    total: number;
}

export interface ProjectType {
    id: number;
    attributes: {
        title: {
            ru: string;
            en: string;
        };
        description: {
            ru: string;
            en: string;
        };
        category: {
            ru: string;
            en: string;
        };
        stacks: {
            ru: string[];
            en: string[];
        };
        link: string;
    };
    image: FluidImageType;
    links: {
        self: string;
    }[];
}
